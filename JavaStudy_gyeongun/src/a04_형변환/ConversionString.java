package a04_형변환;

public class ConversionString {

	/**
	 * 
	 * String(문자열)
	 * 
	 */
	public static void main(String[] args) {
		char name1 = '김';
		char name2 = '준';
		char name3 = '일';

		System.out.print(name1);
		System.out.print(name2);
		System.out.print(name3);

		String name = "김준일";

		System.out.println("" + name1 + name2 + name3 + "입니다.");
		System.out.println(name + "입니다.");
		System.out.println();
	}
}
